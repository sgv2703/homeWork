package lesson3;

public class Fruit {
    private int amount;
    private float weight;

    public Fruit (float weight) {
        this.weight = weight;
    }

    public int getAmount() {
        return amount;
    }

    public float getWeight() {
        return weight;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }
}
